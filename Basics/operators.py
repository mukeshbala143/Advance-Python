
a = 5   
b = 3   

comp_a = ~a  
print(f"Complement of {a} (~a): {comp_a}")

and_result = a & b 
print(f"{a} & {b} = {and_result}")

or_result = a | b  
print(f"{a} | {b} = {or_result}")

xor_result = a ^ b  
print(f"{a} ^ {b} = {xor_result}")

left_shift = a << 1  
print(f"{a} << 1 = {left_shift}")

right_shift = a >> 1  
